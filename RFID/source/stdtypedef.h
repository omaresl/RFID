#ifndef STDTYPEDEF_H_
#define STDTYPEDEF_H_

typedef unsigned char 	T_UBYTE;
typedef unsigned short 	T_UWORD;
typedef unsigned int	T_ULONG;

#define FALSE 	((T_UBYTE)0U)
#define TRUE	((T_UBYTE)1U)

#define nullptr	0

#endif /* STDTYPEDEF_H_ */
